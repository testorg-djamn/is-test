    private void addBookToSystem() {
        boolean isbnAlreadyExists = false;
        for (int i = 0; i < jsonArray.size(); i++) {
            JSONObject jsonObject = (JSONObject) jsonArray.get(i);
            Object book = jsonObject.get("Book");
            JSONObject bookObj = (JSONObject) book;
            if (bookObj.get("ISBN").toString().equals(jTextField4Isbn.getText())) {
                JOptionPane.showMessageDialog(this, "This ISBN already exists!", "Error!", JOptionPane.ERROR_MESSAGE);
                isbnAlreadyExists = true;
                break;
            }
        }
        if (!isbnAlreadyExists) {
            JSONObject jsonObject = new JSONObject();
            if (destinationFile != null) {
                jsonObject.put("Cover", "" + destinationFile.toPath() + "");
            } else {
                jsonObject.put("Cover", "X");
            }

            jsonObject.put("Title", jTextField1Title.getText());
            jsonObject.put("Author", jTextField2Author.getText());
            jsonObject.put("Pages", jTextField3Pages.getText());
            jsonObject.put("ISBN", jTextField4Isbn.getText());
            jsonObject.put("Location", jTextField5Location.getText());
            jsonObject.put("Availability", "Available"); // Beim Hinzufügen ist es immer Available, bei Edit wird dann getAvailability verwendet
            JSONObject bookObj = new JSONObject();
            bookObj.put("Book", jsonObject);
            jsonArray.add(bookObj);
            try {
                if (sourceFile != null && destinationFile != null) {
                    Files.copy(sourceFile.toPath(), destinationFile.toPath());
                }
                writeFile();
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "This icon already exists!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void editBook() {
        for (int i = 0; i < jsonArray.size(); i++) {
            JSONObject jsonObject = (JSONObject) jsonArray.get(i);
            Object book = jsonObject.get("Book");
            JSONObject bookObj = (JSONObject) book;
            if (bookObj.get("ISBN").toString().equals(jTextField4Isbn.getText())) {
                if (destinationFile != null) {
                    bookObj.put("Cover", "" + destinationFile.toPath() + "");
                    if (oldFile != null) {
                        if (!oldFile.getPath().equals(destinationFile.getPath())) {
                            oldFile.delete();
                        }
                    }
                } else {
                    bookObj.put("Cover", "X");
                    if (oldFile != null) {
                        oldFile.delete();
                    }
                }
                bookObj.put("Title", jTextField1Title.getText());
                bookObj.put("Author", jTextField2Author.getText());
                bookObj.put("Pages", jTextField3Pages.getText());
                bookObj.put("ISBN", jTextField4Isbn.getText());
                bookObj.put("Location", jTextField5Location.getText());
                jsonObject.put("Book", bookObj);

                try {
                    if (sourceFile != null && destinationFile != null) {
                        Files.copy(sourceFile.toPath(), destinationFile.toPath());
                    }
                    writeFile();
                } catch (IOException e) {
                    JOptionPane.showMessageDialog(this, "This icon already exists!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }

    private void addToTextField(Book book) {
        if (!book.getCoverLocation().equals("X")) {
            destinationFile = new File(book.getCoverLocation());
            oldFile = new File(book.getCoverLocation());
            ImageIcon icon = new ImageIcon(book.getCoverLocation());
            Image image = icon.getImage(); // transform it
            Image newImg = image.getScaledInstance(jLabel7CoverPreview.getWidth(), jLabel7CoverPreview.getHeight(), java.awt.Image.SCALE_SMOOTH); // scale it the smooth way
            icon = new ImageIcon(newImg);
            jLabel7CoverPreview.setIcon(icon);
        }
        jTextField1Title.setText(book.getTitle());
        jTextField2Author.setText(book.getAuthor());
        jTextField3Pages.setText(book.getPages());
        jTextField4Isbn.setText(book.getIsbn());
        jTextField4Isbn.setEnabled(false);
        jTextField5Location.setText(book.getLocation());

        if (book.getLocation().equals("-/-")) jTextField3Pages.setText("");
    }

    private void readFile() {
        File file = new File("src/main/resources/books.txt");
        if (file.length() != 0) {
            JSONParser jsonParser = new JSONParser();
            try (FileReader fileReader = new FileReader("src/main/resources/books.txt")) {
                Object obj = jsonParser.parse(fileReader);
                jsonArray = (JSONArray) obj;
            } catch (ParseException | IOException e) {
                e.printStackTrace();
            }
        }
    }

    private void writeFile() {
        try (FileWriter fileWriter = new FileWriter("src/main/resources/books.txt")) {
            fileWriter.write(jsonArray.toJSONString());
        } catch (IOException e) {
            e.printStackTrace();
        }
        this.dispose();
    }

