    private void jButton1UploadCoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1UploadCoverActionPerformed
        BufferedImage originalImg = null;

        // Popup für die Bildauswahl
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("JPG & PNG Images", "jpg", "png");
        fileChooser.setFileFilter(filter);

        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            sourceFile = fileChooser.getSelectedFile();
            try {
                originalImg = ImageIO.read(sourceFile);
                destinationFile = new File("src/main/resources/cover/" + sourceFile.getName());
            } catch (Exception e) {
                e.printStackTrace();
            }
            ImageIcon newImg = new ImageIcon(originalImg.getScaledInstance(jLabel7CoverPreview.getWidth(), jLabel7CoverPreview.getHeight(), java.awt.Image.SCALE_SMOOTH));

            jLabel7CoverPreview.setIcon(newImg);
        }
    }//GEN-LAST:event_jButton1UploadCoverActionPerformed

