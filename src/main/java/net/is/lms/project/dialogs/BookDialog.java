    private void jButton3SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3SaveActionPerformed
        if (!jTextField1Title.getText().equals("") && !jTextField2Author.getText().equals("") && !jTextField3Pages.getText().equals("") && !jTextField4Isbn.getText().equals("")) {
            if (!Pattern.matches("[A-Za-z]+", jTextField2Author.getText())) {
                JOptionPane.showMessageDialog(this, "The author should only contain letters!", "Error!", JOptionPane.ERROR_MESSAGE);
            } else if (!Pattern.matches("\\d{10}", jTextField4Isbn.getText())) {
                JOptionPane.showMessageDialog(this, "The ISBN has to be a 10 digit number!", "Error!", JOptionPane.ERROR_MESSAGE);
            } else if (!Pattern.matches("^[1-9]\\d*$", jTextField3Pages.getText())) {
                JOptionPane.showMessageDialog(this, "Only numbers higher than 0 are allowed for pages!", "Error!", JOptionPane.ERROR_MESSAGE);
            } else {
                readFile();

                if (jLabel1BookTitle.getText().equals("Add Book")) {
//                    System.out.println("Add Book?");
                    addBookToSystem();
                }
                if (jLabel1BookTitle.getText().equals("Edit Book")) {
                    editBook();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill out all text fields!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }
//GEN-LAST:event_jButton3SaveActionPerformed

